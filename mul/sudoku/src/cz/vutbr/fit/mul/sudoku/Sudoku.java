    public void generate(int hints) {
        String s = "";;
        try {
        	File file = new File(getClass().getResource("sudoku.txt").toURI());
        	RandomAccessFile rac = new RandomAccessFile(file, "r");
            try{
            	rac.seek(82 * RNG.nextInt((int) (rac.length()/82+1)));
            	s = rac.readLine();
            }finally{
            	rac.close();
            }
        } catch (URISyntaxException | IOException e) {
			e.printStackTrace();
			s = "000000001000000020000003000000040500006000300007810000010020004030000070950000000";
		}

        unsolved.clear();
        clues.clear();
        Arrays.fill(solution, NUMBER[0]);
        Arrays.fill(puzzle, 0);

        for (byte i = 0; i < SIZE2; ++i) {
            int value = Character.getNumericValue(s.charAt(i));
            if (0 < value && value <= 9) {
                solution[i] = NUMBER[value];
                assign(solution[i], i);
                clues.add(i);
            } else {
                unsolved.offer(i);
            }
        }

        solve();

        hints -= clues.size();
        while (hints > 0) {
            if (clues.add((byte) RNG.nextInt(SIZE2))) {
                hints--;
            }
        }
        
        randomize(); 
       
        for (Byte clue : clues) {
        	puzzle[clue] = Integer.numberOfTrailingZeros(solution[clue]) + 1;
		}
    }
    
