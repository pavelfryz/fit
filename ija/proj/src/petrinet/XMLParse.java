package petrinet;

import org.dom4j.Branch;
import org.dom4j.Element;

/**
 * Rozhrani pro prevod do XML.
 * @author Vojtech Bartl - xbartl03, Pavel Fryz - xfryzp00
 */
public interface XMLParse {
  /**
   * Nastavi hodnoty z XML elementu
   * @param branch XML element
   */
  void toXML(Branch branch);
   /**
   * Prida do vetve XML dokumentu element
   * reprezentujici objekt
   * @param element Vetev XML dokumentu
   */
  void fromXML(Element element);
}