package petrinet;

/**
 * Rozhrani tokenu. Obsahuje metody
 * pro praci s tokenem.
 * @author Vojtech Bartl - xbartl03, Pavel Fryz - xfryzp00
 */
public interface Token {
  /**
   * Vrati hodnotu tokenu reprezentovanou
   * retezcovou hodnotou
   * @return Hodnota tokenu
   */
  String getValue();

  /**
   * Nastavi token z retezcove hodnoty
   * @param value Hodnota tokenu
   */
  void setValue(String value);
}